package day02

fun main() {

    //Arrays in kotlin
    val arr = arrayOf(1, 2, 3)
    for (i in arr) {
        print("$i ")
    }
    println()

    val intArr = intArrayOf(10, 20, 30, 40)
    for (i in intArr) {
        print("$i ")
    }
    println()

    val floatArr = floatArrayOf(10.1F, 20.6F, 30.4F, 40.3F)
    for (i in floatArr) {
        print("$i ")
    }
    println()


}



